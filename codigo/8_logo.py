    lock.acquire()
    solicitud_cambio = False
    solicitud_reset  = False
    lock.release()

    # Activar visor y mostrar primer logo
    lock.acquire()
    datos_logo['activo']      = True
    datos_logo['logo_actual'] = 0
    datos_logo['nuevo']       = True
    lock.release()

    # Core 0 se queda en este loop hasta que se presione btn_reset
    core0_logos_loop()

