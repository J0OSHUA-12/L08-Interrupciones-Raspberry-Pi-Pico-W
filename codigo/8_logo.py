def core1_tareas():
    global modo_actual, flag_timer

    ultimo_dht  = 0
    ultimo_vsys = 0

    while True:

        # ---- Pausar si Core 0 esta ejecutando una transicion de modo ----
        lock.acquire()
        transicion = en_transicion
        lock.release()
        if transicion:
            utime.sleep_ms(50)
            continue

        # ---- Obtener modo actual ----
        lock.acquire()
        modo = modo_actual
        lock.release()

        # ---- Actualizar VSYS cada 10 s (para barra de estado en modos 2-5) ----
        ahora = utime.ticks_ms()
        if utime.ticks_diff(ahora, ultimo_vsys) > 10000:
            vs = leer_vsys()
            lock.acquire()
            datos_multi['vsys'] = vs
            lock.release()
            ultimo_vsys = ahora

        # ---- Bajar bandera del timer ----
        if flag_timer:
            flag_timer = False

        # ============================================================
        # MODO 1: VISOR DE LOGOS
        #   Core 1 dibuja el logo en pantalla completa (128x64) cuando
        #   datos_logo['nuevo'] == True. El logo sobreescribe la barra de
        #   estado intencionalmente para maxima visibilidad del bitmap.
        # ============================================================
        if modo == 1:
            lock.acquire()
            activo    = datos_logo['activo']
            hay_nuevo = datos_logo['nuevo']
            logo_idx  = datos_logo['logo_actual']
            lock.release()

            if not activo:
                utime.sleep_ms(50)
                continue

            if hay_nuevo:
                oled.fill(0)
                if 0 <= logo_idx < NUM_LOGOS:
                    # Dibuja bitmap 128x64 en pantalla completa
                    fb = framebuf.FrameBuffer(
                        LOGOS[logo_idx], 128, 64, framebuf.MONO_HLSB)
                    oled.blit(fb, 0, 0)
                oled.show()

                lock.acquire()
                datos_logo['nuevo'] = False
                lock.release()

                nombre = LOGO_NOMBRES[logo_idx] if logo_idx < len(LOGO_NOMBRES) \
                         else str(logo_idx)
                print("Logo mostrado: {}".format(nombre))

            utime.sleep_ms(50)

        # ============================================================
        # MODO 2: VOLTIMETRO
        # ============================================================
        elif modo == 2:
            v = leer_voltaje()
            lock.acquire()
            datos_multi['voltaje'] = v
            lock.release()
            mostrar_voltimetro()
            utime.sleep_ms(200)

        # ============================================================
        # MODO 3: OHMETRO
        # ============================================================
        elif modo == 3:
            rx = leer_resistencia()
            lock.acquire()
            datos_multi['rx']     = rx
            datos_multi['rx_str'] = formatear_resistencia(rx)
            lock.release()
            mostrar_ohmetro()
            utime.sleep_ms(200)

        # ============================================================
        # MODO 4: AMPERIMETRO
        # ============================================================
        elif modo == 4:
            i_ma = leer_corriente()
            lock.acquire()
            datos_multi['corriente_ma'] = i_ma
            lock.release()
            mostrar_amperimetro()
            utime.sleep_ms(200)

        # ============================================================
        # MODO 5: DHT22 + TEMPERATURA INTERNA + VSYS
        # ============================================================
        elif modo == 5:
            t_int = leer_temp_interna()
            ahora = utime.ticks_ms()
            if utime.ticks_diff(ahora, ultimo_dht) > 2000:
                t_dht, h_dht = leer_dht22()
                vs = leer_vsys()
                lock.acquire()
                if t_dht is not None:
                    datos_multi['temp_dht'] = t_dht
                    datos_multi['hum_dht']  = h_dht
                datos_multi['temp_int'] = t_int
                datos_multi['vsys']     = vs
                lock.release()
                ultimo_dht  = ahora
                ultimo_vsys = ahora
            mostrar_dht22()
            utime.sleep_ms(500)

