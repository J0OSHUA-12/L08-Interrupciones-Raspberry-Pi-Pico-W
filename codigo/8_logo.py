logo_idx_actual = 0   # Indice del logo actualmente visible

def core0_logos_loop():
    """Bucle de control del visor de logos.
    btn_set cicla logos sin cambiar de modo.
    btn_reset sale del loop hacia core0_principal (que vuelve a modo 1).
    Entradas:  datos_logo, solicitud_cambio, solicitud_reset (globales)
    Salidas:   datos_logo actualizado, logo_idx_actual actualizado"""
    global logo_idx_actual, solicitud_cambio, solicitud_reset

    while True:

        # btn_reset: salir del loop (Core 0 volverá a ejecutar ejecutar_logos)
        if solicitud_reset:
            lock.acquire()
            datos_logo['activo'] = False
            datos_logo['nuevo']  = False
            lock.release()
            return

        # btn_set: ciclar al siguiente logo (sin cambiar de modo)
        if solicitud_cambio:
            logo_idx_actual = (logo_idx_actual + 1) % NUM_LOGOS
            lock.acquire()
            solicitud_cambio          = False      # Consumir flag aqui, no en core0_principal
            datos_logo['logo_actual'] = logo_idx_actual
            datos_logo['nuevo']       = True
            lock.release()
            print("Cicla logo -> {}".format(
                LOGO_NOMBRES[logo_idx_actual] if logo_idx_actual < len(LOGO_NOMBRES)
                else str(logo_idx_actual)))

        utime.sleep_ms(50)

